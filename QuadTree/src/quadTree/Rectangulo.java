package quadTree;

public class Rectangulo {
	
	private int x,y,lado;

	public Rectangulo(int x, int y, int l) {
		super();
		this.x = x;
		this.y = y;
		this.lado=l;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public int getLado() {
		return lado;
	}

	public void setLado(int h) {
		this.lado = h;
	}
	

}
