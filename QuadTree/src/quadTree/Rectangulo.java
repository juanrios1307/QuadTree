package quadTree;

public class Rectangulo {
	
	private int x,y,h,w;

	public Rectangulo(int x, int y, int h, int w) {
		super();
		this.x = x;
		this.y = y;
		this.h = h;
		this.w = w;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public int getH() {
		return h;
	}

	public void setH(int h) {
		this.h = h;
	}

	public int getW() {
		return w;
	}

	public void setW(int w) {
		this.w = w;
	}
	
	

}
